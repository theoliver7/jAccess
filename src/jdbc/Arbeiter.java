package jdbc;

public class Arbeiter {
	public String idarbeiter;
	public String name;
	public String nachname;
	public String wohnohrt;
	public String funktion;

	public String getIdArbeiter() {
		return idarbeiter;
	}

	public String getIdarbeiter() {
		return idarbeiter;
	}

	public void setIdarbeiter(String idarbeiter) {
		this.idarbeiter = idarbeiter;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNachname() {
		return nachname;
	}

	public void setNachname(String nachname) {
		this.nachname = nachname;
	}

	public String getWohnohrt() {
		return wohnohrt;
	}

	public void setWohnohrt(String wohnohrt) {
		this.wohnohrt = wohnohrt;
	}

	public String getFunktion() {
		return funktion;
	}

	public void setFunktion(String funktion) {
		this.funktion = funktion;
	}



}
